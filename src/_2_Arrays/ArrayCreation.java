package _2_Arrays;

public class ArrayCreation {
    public static void main(String[] args) {
        // Array declaration:

        int my_array_1[];
        int[] my_array_2; // Most suitable index

        // Array Initialization:

        my_array_2 = new int[5];

        // Array Initialization and declaration:

        int arr_1[] = new int[5];
        int[] arr_2 = new int[5];

        // Array Assignment:

        arr_2[0] = 10;
        arr_2[1] = 5;
        arr_2[2] = 3;
        arr_2[3] = 99;
        arr_2[4] = 98;

        // Array Initialization and Declaration and Assignment:

        int array_1[] = {1,2,3,4,5};
        int[] array_2 = {1,2,3,4,5};
    }
}